public class XYZ {
    private double x;
    private double y;
    private double z;
    private int edxid;

    public int getEdxid() {
        return edxid;
    }

    public void setEdxid(int edxid) {
        this.edxid = edxid;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }
}
